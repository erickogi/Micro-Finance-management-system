public int writeCol(String newcol){
    int succesful=0;
    FileWriter writer = null;
      try {
          writer = new FileWriter("dbConfig.txt");
          writer.write(newcol);
          writer.close();
          succesful=1;
      } catch (IOException ex) {
          Logger.getLogger(DbConfig.class.getName()).log(Level.SEVERE, null, ex);
      } finally {
          try {
              writer.close();
          } catch (IOException ex) {
              Logger.getLogger(DbConfig.class.getName()).log(Level.SEVERE, null, ex);
          }
      }
      return succesful;
}
